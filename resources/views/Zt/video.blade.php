    var videoclose = document.getElementsByTagName("video");
    // 暂停函数
    function pauseAll() {
      var self = this;
      [].forEach.call(videoclose, function (i) {
        // 将video中其他的video全部暂停
        i !== self && i.pause();
      })

    }
    // 给play事件绑定暂停函数
    [].forEach.call(videoclose, function (i) {
      i.addEventListener("play", pauseAll.bind(i));
    })


  })

